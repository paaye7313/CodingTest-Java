package level2;

/*
    문제
    정수로 이루어진 배열 numbers가 있습니다.
    배열 의 각 원소들에 대해 자신보다 뒤에 있는 숫자 중에서 자신보다 크면서 가장 가까이 있는 수를 뒷 큰수라고 합니다.
    정수 배열 numbers가 매개변수로 주어질 때,
    모든 원소에 대한 뒷 큰수들을 차례로 담은 배열을 return 하도록 solution 함수를 완성해주세요.
    단, 뒷 큰수가 존재하지 않는 원소는 -1을 담습니다.
    
    2026-03-30
    간단하게 생각하면 for문 두개 돌리면 가능하겠지만
    문제는 number의 최대 길이가 1,000,000이라는 것
    여기서 요점은 '뒤에 있는 숫자중에서 가장 큰 숫자'가 아니라 '자신보다 크면서 가장 가까이 있는 수'
    그러므로 매번 뒷 큰수를 찾는 것이 아닌 뒷 큰수 하나를 찾아 놓고 
    다음 목표숫자가 이 뒷 큰수보다 작으면 찾는 과정을 생략하고 바로 저장하면 효율성이 더 좋지 않을가 생각함
    필요한 변수들 : 저장할 List , 뒷 큰수, 검색 포인트
    하지만 채점 과정에서 오답들이 발생

    예를들어 [5, 3, 5, 6]일 경우 0번 5에 기준에서는 6이 뒷 큰수 이지만 그 다음인 1번 3의 경우 5가 뒷 큰수가 됨
    검색 포인트 대신 작은수 변수 도입 작은수는 뒷 큰수를 저장 할때 목표숫자를 저장함
    채점 과정에서 오답들이 거의 사라졌지만 소수의 문제가 시간초과로 오답

    긴 시간 고민한 결과 문자열을 뒤부터 탐색하면 어떨까 생각
    뒤에서부터 역순으로 목표숫자를 가장 큰 수 와 비교해서 크거나 같으면 -1을 저장하고 가장 큰 수와 뒷 큰수 갱신
    가장 큰 수보다 작지만 작은수 이상이고 뒷 큰수보다 작을 경우 뒷 큰수 저장
    가장 큰 수보다 작고 작은수 보다도 작으면 가장 큰 수 까지 정순으로 검색 후 뒷 큰수 가 있으면 뒷 큰수 갱신후 저장, 작은수 갱신
        뒷 큰수 가 없으면 가장 큰 수 저장
    마지막 출력은 List를 역순으로 출력
    필요한 변수 : 저장할 List, 가장 큰 수, 뒷 큰수, 작은수
    하지만 채점에서 실패

    이제 AI에게 질문해봄
    역순으로 진행 하는 것은 맞음, 하지만 스택을 활용함
    현재 원소보다 작거나 같은 스택 원소는 제거 (그들의 뒷 큰수가 될 수 없음)
    스택 top이 현재 원소보다 크면 그것이 뒷 큰수
    현재 원소를 스택에 추가

    간단한 문제를 너무 돌아간 느낌이 듬
 */
import java.util.*;

class Solution {
    public int[] solution(int[] numbers) {
        int n = numbers.length;
        int[] answer = new int[n];
        Stack<Integer> stack = new Stack<>();

        // 배열을 역순으로 순회 (뒤에서 앞으로)
        for (int i = n - 1; i >= 0; i--) {
            // 현재 원소보다 작거나 같은 스택 원소 제거
            while (!stack.isEmpty() && stack.peek() <= numbers[i]) {
                stack.pop();
            }

            // 스택이 비어있으면 뒷 큰수 없음
            if (stack.isEmpty()) {
                answer[i] = -1;
            } else {
                // 스택 top이 뒷 큰수
                answer[i] = stack.peek();
            }

            // 현재 원소를 스택에 추가
            stack.push(numbers[i]);
        }

        return answer;
    }
}